                <h3>Janvier 2020 – École Saint Joseph l’Estaque, Marseille (13)</h3>
                <p>Je viens par cet email vous annoncer que nous avons mis en place SOL! dans l’établissement depuis le 9 décembre 2021. Les retombées ont été immédiates et inattendues : des enfants n’aimant pas les livres se sont rapidement mis à lire. Lors du marché de Noël, une maman m’expliquait que son fils qui avait en horreur les livres et la lecture, imposait désormais à la maison de ne pas être dérangé quand il lisait. Et cela arrivait souvent ! Ce témoignage, entre autres, m’a conforté sur le bien fondé du projet. Je vous remercie pour l’accompagnement téléphonique qui a été bien utile pour me permettre de mobiliser l’équipe. Le dossier est complet et très intéressant, chaque enseignante a pu s’en imprégner. Mme G., Chef d’établissement </p>
            </article>

            <article>
                <h3>Novembre 2019 – École Primaire, Caen (14)</h3>
                <p>J’ai eu l’occasion d’effectuer un remplacement dans une école en zone d’éducation prioritaire. J’ai vu la joie des enfants qui pour certains en CE1 découvraient des livres enfin adaptés à leur niveau de lecture, se rendaient régulièrement en bibliothèque et demandaient à leurs parents des livres en cadeaux d’anniversaire mais aussi découvraient pour la 1e fois des articles de journaux adaptés à leur âge, des bandes dessinées pour ceux qui n’y voyaient pas de sens dans un premier temps… des familles concernées après la présentation du projet par la Directrice… Bref, un grand moment d’apaisement, de retour au calme pour des enfants dont la concentration est aussi à développer, un projet enthousiasmant pour toute l’équipe de l’école Primaire. Mme M., enseignante</p>
            </article>

