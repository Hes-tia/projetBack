                <h3>Novembre 2018 – Collège REP+, Villefranche sur Saône (69)</h3>
                <p>Le projet fédère l’équipe des profs : de manière surprenante, ce sont les profs des matières les moins littéraires, sciences et EPS, qui se montrent les plus enthousiastes. Le projet permet de créer d’autres types de lien avec les élèves : certains collègues discutent avec eux de leurs lectures, leur proposent des lectures offertes quand il reste 5 minutes avant la fin du cours, ou leur font part de leurs lectures coups de cœur ou en lien avec les chapitres étudiés. Ces 10 minutes de lecture calment et apaisent les élèves pour le cours suivant. Le taux de fréquentation du CDI a augmenté et le documentaliste dit voir des élèves qu’il ne voyait jamais auparavant. Mme G., professeur</p>
